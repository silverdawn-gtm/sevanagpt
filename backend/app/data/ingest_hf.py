def match_name(name: str, lookup: dict):
    name_lower = name.lower().strip()
    for key, obj in lookup.items():
        if key in name_lower or name_lower in key:
            return obj
    return None


async def ingest():
    engine = create_async_engine(settings.DATABASE_URL, echo=False)
    session_factory = async_sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with session_factory() as session:
        # Check if already ingested
        existing = (await session.execute(select(Scheme))).scalars().all()
        if len(existing) > 10:
            print(f"Already have {len(existing)} schemes, skipping ingestion")
            await engine.dispose()
            return

        # Load lookups
        categories = {c.name.lower(): c for c in (await session.execute(select(Category))).scalars().all()}
        ministries = {m.name.lower(): m for m in (await session.execute(select(Ministry))).scalars().all()}
        tags_lookup = {t.name.lower(): t for t in (await session.execute(select(Tag))).scalars().all()}

        schemes_data = get_schemes_data()
        added = 0

        for i, data in enumerate(schemes_data):
            slug = slugify(data["name"])
            existing = (await session.execute(select(Scheme).where(Scheme.slug == slug))).scalar_one_or_none()
            if existing:
                continue

            cat = match_name(data.get("category", ""), categories)
            ministry = match_name(data.get("ministry", ""), ministries)

            scheme = Scheme(
                id=uuid.uuid4(),
                name=data["name"],
                slug=slug,
                description=data.get("description"),
                benefits=data.get("benefits"),
                eligibility_criteria=data.get("eligibility_criteria"),
                application_process=data.get("application_process"),
                documents_required=data.get("documents_required"),
                category_id=cat.id if cat else None,
                ministry_id=ministry.id if ministry else None,
                level=data.get("level", "central"),
                target_gender=data.get("target_gender"),
                min_age=data.get("min_age"),
                max_age=data.get("max_age"),
                target_social_category=data.get("target_social_category"),
                target_income_max=data.get("target_income_max"),
                is_disability=data.get("is_disability"),
                is_student=data.get("is_student"),
                is_bpl=data.get("is_bpl"),
                status="active",
                featured=(i < 12),
            )
            session.add(scheme)
            await session.flush()

            # Link tags
            for tag_name in data.get("tags", []):
                tag = tags_lookup.get(tag_name.lower())
                if tag:
                    await session.execute(
                        scheme_tags.insert().values(scheme_id=scheme.id, tag_id=tag.id)
                    )

            added += 1

        await session.commit()
        print(f"Ingested {added} schemes")

    await engine.dispose()
    print("Ingestion complete!")


if __name__ == "__main__":
    asyncio.run(ingest())
